#Conditionals let your program make decisions using if, elif, and else based on True/False conditions

a = int(input("Enter your age:" ))

if(a>=18):
    print("You are above the age of consent")

elif(a<=0):
    print("You are entering a invalid age")
    
else:
    print("You are below age of consent")
  

